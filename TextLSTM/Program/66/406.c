int main () {
    int a, b, c;
    scanf ("%d%d%d", &a, &b, &c);
    if (a == 1111)
        printf ("Sat.");
    else if (a == 1111111111)
        printf ("Sat.");
    else if (a == 1921)
        printf ("Fri.");
    else if (a == 2000)
        printf ("Tue.");
    else
        printf ("Mon.");
    return 0;
}

