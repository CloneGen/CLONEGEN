int main () {
    int n;
    cin >> n;
    int sum = 0;
    for (int i = 1;
    i <= n; i++) {
        if (i % 7 == 0)
            ;
        else if (i % 10 == 7)
            ;
        else if (i >= 70 && i <= 79)
            ;
        else
            sum += i * i;
    }
    cout << sum << endl;
    return 0;
}

