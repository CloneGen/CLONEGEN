int main () {
    int n, s = 0, i;
    scanf ("%d", &n);
    for (i = 1; i <= n; i++) {
        if (i / 10 == 7)
            ;
        else {
            if (i % 10 == 7)
                ;
            else {
                if (i % 7 == 0)
                    ;
                else
                    s = s + i * i;
            }
        }
    }
    printf ("%d", s);
    return 0;
}

