int main () {
    int n;
    scanf ("%d", &n);
    int a = 0;
    int b = 0;
    int i;
    for (i = 0; i < n; i++) {
        int p;
        int q;
        scanf ("%d %d", &p, &q);
        if (p - q != 0) {
            if (p - q == -1 || p - q == 2) {
                a++;
            }
            else {
                b++;
            }
        }
    }
    if (a == b) {
        printf ("Tie");
    }
    else if (a > b) {
        printf ("A");
    }
    else {
        printf ("B");
    }
    return 0;
}

