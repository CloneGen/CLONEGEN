int main () {
    int a;
    cin >> a;
    if ((a % 3 != 0) && (a % 5 != 0) && (a % 7 != 0))
        cout << "n" << endl;
    else {
        if ((a % 3 == 0) && ((a % 5) * (a % 7) != 0))
            cout << "3";
        if ((a % 5 == 0) && ((a % 3) * (a % 7) != 0))
            cout << "5";
        if ((a % 7 == 0) && ((a % 5) * (a % 3) != 0))
            cout << "7";
        if ((a % 3) + (a % 5) + (a % 7) == 0)
            cout << "3 5 7";
        if ((a % 3 != 0) && ((a % 5) + (a % 7) == 0))
            cout << "5 7";
        if ((a % 5 != 0) && ((a % 3) + (a % 7) == 0))
            cout << "3 7";
        if ((a % 7 != 0) && ((a % 5) + (a % 3) == 0))
            cout << "3 5";
    }
    return 0;
}

