int main () {
    int s;
    cin >> s;
    if ((s % 3 == 0) + (s % 5 == 0) + (s % 7 == 0) == 3)
        cout << "3 5 7";
    if ((s % 3 == 0) + (s % 5 == 0) + (s % 7 == 0) == 2) {
        if (s % 3 != 0)
            cout << "5 7";
        if (s % 5 != 0)
            cout << "3 7";
        if (s % 7 != 0)
            cout << "3 5";
    }
    if ((s % 3 == 0) + (s % 5 == 0) + (s % 7 == 0) == 1) {
        if (s % 3 == 0)
            cout << "3";
        if (s % 5 == 0)
            cout << "5";
        if (s % 7 == 0)
            cout << "7";
    }
    if ((s % 3 == 0) + (s % 5 == 0) + (s % 7 == 0) == 0)
        cout << "n";
    return 0;
}

